package Ex3.update;

public class AgentInfo {
    private int uncompletedMissions;
    private int successStrings;

    public int getUncompletedMissions() {
        return uncompletedMissions;
    }

    public void setUncompletedMissions(int uncompletedMissions) {
        this.uncompletedMissions = uncompletedMissions;
    }

    public int getSuccessStrings() {
        return successStrings;
    }

    public void setSuccessStrings(int successStrings) {
        this.successStrings = successStrings;
    }
}
